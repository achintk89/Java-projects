import java.util.Scanner;

public class DoubleSum {

        public static void main(String[] args)
        {
            Scanner s = new Scanner(System.in);
            System.out.println("Enter two double values");
            double d1, d2;
            d1 = s.nextDouble();
            d2 = s.nextDouble();
            System.out.println("Sum: "+(d1+d2));


        }

    }

